    public void set_pause()
    {
        pause=0;
        mytimerm=new Timer();
        mytask1=new Timertask()
    {
     public void run()
     {  
     
     System.out.println(" "+time);
     if (pause==0)
     {
         time-=1;
     }
     jLabel4.setText(" "+time);
     count+=1;
     if ((count%2==0)&&(count!=0))
     {
     System.out.println("refresh!!"+count);
     penal1.x1=penal1.rand.nextInt(900);
     penal1.y1=penal1.rand.nextInt(550);
     penal1.x2=penal1.rand.nextInt(900);
     penal1.y2=penal1.rand.nextInt(550);
     penal1.repaint();
     }
     if (time==0)
     {
      
      showmark another=new showmark();  
      another.set_mark(marks);
      another.setVisible(true);
      
      dispose();
      
      }
     }
    };
        mytimerm.scheduleAtFixedRate(mytask1, 1000, 1000);
    }
