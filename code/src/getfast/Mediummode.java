    private void penal1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_penal1MousePressed
      cx=evt.getX();
    cy=evt.getY();
    if ((cx>penal1.x1)&&(cx<penal1.x1+60)&&(cy>penal1.y1)&&(cy<penal1.y1+60))
    {    
        count=0;
         marks+=1;
         jLabel5.setText(" "+marks);
         penal1.x1=penal1.rand.nextInt(900);
         penal1.y1=penal1.rand.nextInt(550);
         penal1.x2=penal1.rand.nextInt(900);
         penal1.y2=penal1.rand.nextInt(550);
         penal1.repaint();
         if(sound==1){Sound.Sound5.play();}
    }                                          
 if ((cx>penal1.x2)&&(cx<penal1.x2+60)&&(cy>penal1.y2)&&(cy<penal1.y2+60))
    { 
         marks-=1;
         jLabel5.setText(" "+marks);
    }    // TODO add your handling code here:
    }//GEN-LAST:event_penal1MousePressed
